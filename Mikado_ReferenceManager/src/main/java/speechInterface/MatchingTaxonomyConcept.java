public class MatchingTaxonomyConcept {
	String taxonomyID;
	String domainCode;
	String objectTypeID;
	String objectTypeCode;
	String objectTypeDescription;
	String dataTypeID;
	String dataTypeCode;
	String dataTypeDescription;
	public String getTaxonomyID() {
		return taxonomyID;
	}
	public void setTaxonomyID(String taxonomyID) {
		this.taxonomyID = taxonomyID;
	}
	public String getObjectTypeID() {
		return objectTypeID;
	}
	
	
	public String getDomainCode() {
		return domainCode;
	}
	public void setDomainCode(String domainCode) {
		this.domainCode = domainCode;
	}
	public void setObjectTypeID(String objectTypeID) {
		this.objectTypeID = objectTypeID;
	}
	public String getObjectTypeCode() {
		return objectTypeCode;
	}
	public void setObjectTypeCode(String objectTypeCode) {
		this.objectTypeCode = objectTypeCode;
	}
	public String getObjectTypeDescription() {
		return objectTypeDescription;
	}
	public void setObjectTypeDescription(String objectTypeDescription) {
		this.objectTypeDescription = objectTypeDescription;
	}
	public String getDataTypeID() {
		return dataTypeID;
	}
	public void setDataTypeID(String dataTypeID) {
		this.dataTypeID = dataTypeID;
	}
	public String getDataTypeCode() {
		return dataTypeCode;
	}
	public void setDataTypeCode(String dataTypeCode) {
		this.dataTypeCode = dataTypeCode;
	}
	public String getDataTypeDescription() {
		return dataTypeDescription;
	}
	public void setDataTypeDescription(String dataTypeDescription) {
		this.dataTypeDescription = dataTypeDescription;
	}
	
	
}
