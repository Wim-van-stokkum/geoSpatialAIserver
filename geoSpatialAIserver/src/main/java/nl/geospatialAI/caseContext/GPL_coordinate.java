package nl.geospatialAI.caseContext;

public class GPL_coordinate {
	
	// iso 6709
	
   private String latitude;
   private String longitude;
   private String height_or_depth;
   
   
   public String getLatitude() {
	return latitude;
}
public void setLatitude(String latitude) {
	this.latitude = latitude;
}
public String getLongitude() {
	return longitude;
}
public void setLongitude(String longitude) {
	this.longitude = longitude;
}
public String getHeight_or_depth() {
	return height_or_depth;
}
public void setHeight_or_depth(String height_or_depth) {
	this.height_or_depth = height_or_depth;
}

   
   
}
